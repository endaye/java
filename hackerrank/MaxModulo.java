package hackerrank;

import java.util.*;

public class MaxModulo {

	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
        int t = in.nextInt();
        for (int i = 0; i < t; i++) { // for each test case
            int n = in.nextInt();
            int m = in.nextInt();
            int[] a = new int[n];
            for (int j = 0; j < n; j++)
                a[j] = in.nextInt();
            int max = 0;
            for (int j = 0; j < n; j++) { // for each start index
            	int sum = 0;
            	for (int k = j; k < n; k++) {
            		sum += a[k];
            		int cur = sum % m;
            		max = Math.max(max, cur);
            	}
            }
            System.out.println(max);
        }
        in.close();
	}
}

